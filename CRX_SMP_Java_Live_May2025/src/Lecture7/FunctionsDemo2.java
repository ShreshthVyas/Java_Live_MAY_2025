package Lecture7;

public class FunctionsDemo2 {
	
	public static void main(String[] args) {
		System.out.println("HEY");
		add();
		System.out.println("MID");
		sub();
		System.out.println("END");
	}
	
	public static void add() {
		int a = 10;
		int b = 20;
		int c =  a+b;
		System.out.println(c);
	}
	
	public static void sub() {
		int a = 10;
		int b = 20;
		int c =  b-a;
		System.out.println(c);
	}
}
